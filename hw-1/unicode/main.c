/* Encode a Unicode code point as UTF-8. Returns number of bytes written (1-4). */
static int encode_utf8(uint32_t cp, uint8_t *buf)
{
    if (cp <= 0x7F) {
        buf[0] = (uint8_t)cp;
        return 1;
    }
    if (cp <= 0x7FF) {
        buf[0] = (uint8_t)(0xC0 | (cp >> 6));
        buf[1] = (uint8_t)(0x80 | (cp & 0x3F));
        return 2;
    }
    if (cp <= 0xFFFF) {
        buf[0] = (uint8_t)(0xE0 | (cp >> 12));
        buf[1] = (uint8_t)(0x80 | ((cp >> 6) & 0x3F));
        buf[2] = (uint8_t)(0x80 | (cp & 0x3F));
        return 3;
    }
    buf[0] = (uint8_t)(0xF0 | (cp >> 18));
    buf[1] = (uint8_t)(0x80 | ((cp >> 12) & 0x3F));
    buf[2] = (uint8_t)(0x80 | ((cp >> 6) & 0x3F));
    buf[3] = (uint8_t)(0x80 | (cp & 0x3F));
    return 4;
}

static const uint32_t *select_table(const char *encoding)
{
    if (strcasecmp(encoding, "cp1251") == 0 ||
        strcasecmp(encoding, "cp-1251") == 0 ||
        strcasecmp(encoding, "windows-1251") == 0) {
        return cp1251_table;
    }
    if (strcasecmp(encoding, "koi8r") == 0 ||
        strcasecmp(encoding, "koi8-r") == 0) {
        return koi8r_table;
    }
    if (strcasecmp(encoding, "iso88595") == 0 ||
        strcasecmp(encoding, "iso-8859-5") == 0 ||
        strcasecmp(encoding, "iso8859-5") == 0) {
        return iso88595_table;
    }
    return NULL;
}

int main(int argc, char *argv[])
{
    if (argc != 4) {
        fprintf(stderr, "Usage: %s <input_file> <encoding> <output_file>\n",
                argv[0]);
        fprintf(stderr, "Encodings: cp1251, koi8-r, iso-8859-5\n");
        return EXIT_FAILURE;
    }

    const char *input_path = argv[1];
    const char *encoding = argv[2];
    const char *output_path = argv[3];

    const uint32_t *table = select_table(encoding);
    if (!table) {
        fprintf(stderr, "Unknown encoding: %s\n", encoding);
        fprintf(stderr, "Supported: cp1251 (cp-1251), koi8r (koi8-r), "
                        "iso88595 (iso-8859-5)\n");
        return EXIT_FAILURE;
    }

    FILE *fin = fopen(input_path, "rb");
    if (!fin) {
        perror(input_path);
        return EXIT_FAILURE;
    }

    FILE *fout = fopen(output_path, "wb");
    if (!fout) {
        perror(output_path);
        fclose(fin);
        return EXIT_FAILURE;
    }

    int ch;
    uint8_t utf8_buf[4];

    while ((ch = fgetc(fin)) != EOF) {
        uint32_t cp;
        if (ch < 0x80) {
            cp = (uint32_t)ch;
        } else {
            cp = table[ch - 0x80];
        }

        int len = encode_utf8(cp, utf8_buf);
        if (fwrite(utf8_buf, 1, (size_t)len, fout) != (size_t)len) {
            perror("write error");
            fclose(fin);
            fclose(fout);
            return EXIT_FAILURE;
        }
    }

    if (ferror(fin)) {
        perror("read error");
        fclose(fin);
        fclose(fout);
        return EXIT_FAILURE;
    }

    fclose(fin);
    fclose(fout);
    return EXIT_SUCCESS;
}
