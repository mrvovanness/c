#!/usr/bin/env bash
#
# analyze.sh — простой анализ собранной трассы системных вызовов.
#
# Считает, сколько раз каждый системный вызов был выполнен (по событиям
# sys_enter_*) и формирует частотную таблицу — аналог `strace -c`, но на
# данных, собранных именно ftrace.
#
# Запуск: ./analyze.sh results/10_syscalls.trace
set -euo pipefail

TRACE=${1:-results/10_syscalls.trace}
[ -f "$TRACE" ] || { echo "Нет файла трассы: $TRACE" >&2; exit 1; }

echo "Источник: $TRACE"
echo

# Событие входа в системный вызов печатается ftrace как  "sys_ИМЯ(аргументы)",
# а событие выхода — как  "sys_ИМЯ -> код".  Считаем именно ВХОДЫ: строки,
# где сразу после имени идёт открывающая скобка.
total=$(grep -cE ': sys_[a-z0-9_]+\(' "$TRACE" || true)
echo "Всего перехвачено системных вызовов (событий входа): $total"
echo
printf "%6s  %s\n" "КОЛ-ВО" "СИСТЕМНЫЙ ВЫЗОВ"
printf "%6s  %s\n" "------" "---------------"

grep -oE ': sys_[a-z0-9_]+\(' "$TRACE" \
    | sed -E 's/.*: sys_//; s/\($//' \
    | sort \
    | uniq -c \
    | sort -rn \
    | awk '{ printf "%6d  %s\n", $1, $2 }'
